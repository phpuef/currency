    /** @var array<string, string>|null */
    private static ?array $list = null;

    /**
     * @throws \InvalidArgumentException
     */
    public static function from(string $code): string
    {
        $constants = self::cases();

        return $constants[$code] ?? throw new \InvalidArgumentException(sprintf("Unknown currency code '%s'", $code));
    }

    public static function exists(string $code): bool
    {
        return array_key_exists($code, self::cases());
    }

    /**
     * @return array<string, string>
     */
    public static function cases(): array
    {
        if (self::$list === null) {
            $ref = new \ReflectionClass(self::class);

            /** @var array<string, string> $list */
            $list = $ref->getConstants();
            self::$list = $list;
            unset($list);
        }

        return self::$list;
    }

